const a = 7 // = atribução
let b = 3
b += a // += Atribuição aditiva, acrescenta o valor de b ao a. b= b + a
console.log(b)

b -= 4 // -= Atribuição de subtração. Esta subtraindo 4 do valor de b. b= b - 4
console.log(b)

b *=2  // *= Atribuição de multiplicação. b= b *2
console.log(b)

b /= 2  // /= Atribuição de divisão. b= b / 2
console.log(b)

b %= 2  // %= Resto da divisão. Seo o valor for PAR recebe 0 se for IMPAR recebe 1
console.log(b)