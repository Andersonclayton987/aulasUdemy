// Blocos de repetição For
// a var não tem escopo de bloco ou seja ela será visível tanto dentro como fora do bloco
// o for tem três partes são elas: a DECLARAÇÃO da VARIÁVEL, depois do ponto e virgula a CONDIÇÂO e o INCREMENTO

